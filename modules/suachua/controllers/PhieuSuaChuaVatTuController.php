    public function actionIndex($phieu_sua_chua)
    {   
        $phieuSuaChua=PhieuSuaChua::findOne($phieu_sua_chua); 
        $searchModel = new PhieuSuaChuaVatTuSearch();
        $searchModel->id_phieu_sua_chua=$phieu_sua_chua;
  		if(isset($_POST['search']) && $_POST['search'] != null){
            $dataProvider = $searchModel->search(Yii::$app->request->post(), $_POST['search']);
        } else if ($searchModel->load(Yii::$app->request->post())) {
            $searchModel = new PhieuSuaChuaVatTuSearch(); // "reset"
            $dataProvider = $searchModel->search(Yii::$app->request->post());
        } else {
            $dataProvider = $searchModel->search(Yii::$app->request->queryParams);
        }    
        return $this->render('index', [
            'searchModel' => $searchModel,
            'dataProvider' => $dataProvider,
            "phieuSuaChua"=>$phieuSuaChua
        ]);
    }


